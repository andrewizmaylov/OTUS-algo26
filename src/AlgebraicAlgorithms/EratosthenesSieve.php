class EratosthenesSieve
{
    /**
     * Линейное решето Эратосфена (Решето с минимальным простым делителем)
     * Находит все простые числа до n и минимальный простой делитель для каждого числа.
     *
     * @param int $n Верхняя граница (включительно).
     * @return array Массив с двумя ключами:
     *               'primes' - список простых чисел,
     *               'lp' - массив минимальных простых делителей для чисел от 0 до n.
     */
    public static function handle(int $n): array {
        // 1. Инициализация
        // Массив для минимальных простых делителей (lp = least prime).
        // Изначально заполняем нулями. Индекс массива = число.
        $lp = array_fill(0, $n + 1, 0);

        // Массив для хранения найденных простых чисел.
        $primes = [];

        // 2. Главный цикл: перебираем числа-кандидаты от 2 до n.
        // В алгоритме это переменная i.
        for ($i = 2; $i <= $n; $i++) {

            // 3. Шаг 1: Если число еще не имеет делителя, значит оно простое.
            // Проверяем, равен ли lp[i] нулю (т.е. мы еще не нашли его делитель).
            if ($lp[$i] === 0) {
                // Это простое число.
                $lp[$i] = $i;          // Минимальный делитель простого числа - оно само.
                $primes[] = $i;        // Добавляем число в список простых.
            }

            // 4. Шаг 2: Перебираем простые числа из списка, чтобы "пометить" составные.
            // Это внутренний цикл (в алгоритме - для p из pr).
            // Условия цикла:
            // а) Берем очередное простое число $p из списка $primes.
            // б) Продолжаем, пока $p <= $lp[$i] (это ключевое условие для линейности).
            // в) И пока $p * $i <= $n (чтобы не выйти за пределы).
            foreach ($primes as $p) {
                // Рассчитываем число, которое хотим пометить.
                $composite = $p * $i;

                // Если результат превышает n, дальше считать смысла нет (все будет больше).
                if ($composite > $n) {
                    break;
                }

                // Самая важная строка: записываем минимальный делитель для составного числа.
                // Так как мы перебираем простые числа по возрастанию,
                // текущее $p и будет минимальным простым делителем для $p * $i.
                $lp[$composite] = $p;

                // КЛЮЧЕВОЕ УСЛОВИЕ для линейной сложности:
                // Если $p стало равно минимальному делителю текущего числа $i,
                // значит, мы дошли до границы. Дальнейшая проверка с бОльшими $p
                // приведет к тому, что мы будем помечать числа повторно.
                // Поэтому выходим из цикла.
                if ($p == $lp[$i]) {
                    break;
                }
            }
        }
        return $primes;
        // Возвращаем результат: список простых и массив делителей.
//        return [
//            'primes' => $primes,
//            'lp' => $lp
//        ];
    }
}

